def first_last_6(nums: list[int]) -> bool:
    """
    Проверяет, является ли цифра 6 первым или последним элементом в списке целых чисел.

    Аргументы:
        nums: Список целых чисел длиной 1 или более.

    Возвращает:
        True, если 6 — первый или последний элемент списка, иначе False.
    """
    return nums[0] == 6 or nums[-1] == 6

# Примеры использования
print(first_last_6([6, 1, 2, 3]))   # True
print(first_last_6([1, 2, 3, 6]))   # True
print(first_last_6([1, 6, 2, 3]))   # False
